function getLang(){ return localStorage.getItem("gotabanim_lang")||"kk"; }

function applyLang(lang){
  localStorage.setItem("gotabanim_lang", lang);
  const t = T[lang];
  document.querySelectorAll("[data-i18n]").forEach(el=>{
    const k = el.getAttribute("data-i18n");
    if(t[k]!==undefined) el.textContent = t[k];
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el=>{
    const k = el.getAttribute("data-i18n-placeholder");
    if(t[k]!==undefined) el.placeholder = t[k];
  });
  const sel = document.getElementById("langSelect");
  if(sel) sel.value = lang;
  if(typeof renderProducts==="function") renderProducts();
  if(typeof renderCombos==="function") renderCombos();
}

document.addEventListener("DOMContentLoaded",()=>{
  const lang = getLang();
  const sel = document.getElementById("langSelect");
  if(sel){ sel.value=lang; sel.addEventListener("change",()=>applyLang(sel.value)); }
  applyLang(lang);
});
